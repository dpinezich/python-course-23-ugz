class Station:
    def __init__(self, name, nox, no, no2, o3):
        self.name = name
        self.nox = nox
        self.no = no
        self.no2 = no2
        self.o3 = o3

    def __gt__(self, other):
        if self.o3 > other.o3:
            return True
        else:
            return False

    def __lt__(self, other):
        if self.o3 < other.o3:
            return True
        else:
            return False

    def __str__(self):
        return f"Hello from {self.name}"


class AdvancedStation(Station):
    def __init__(self, name, pm10, pm25, nox, no, no2, o3):
        super().__init__(name, nox, no, no2, o3)
        self.pm10 = pm10
        self.pm25 = pm25

        def __gt__(self, other):
            if self.pm10 > other.pm10:
                return True
            else:
                return False

        def __lt__(self, other):
            if self.pm10 < other.pm10:
                return True
            else:
                return False

        def __str__(self):
            return f"Hello from the more modern {self.name}"


